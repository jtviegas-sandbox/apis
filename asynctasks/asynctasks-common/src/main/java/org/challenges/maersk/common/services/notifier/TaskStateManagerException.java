package org.challenges.maersk.common.services.notifier;

public class TaskStateManagerException extends Exception {

	private static final long serialVersionUID = 1L;

	public TaskStateManagerException() {
		super();
	}

	public TaskStateManagerException(String arg0) {
		super(arg0);
	}

	public TaskStateManagerException(Throwable arg0) {
		super(arg0);
	}

	public TaskStateManagerException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
